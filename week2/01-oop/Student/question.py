class Dog:
    # Initializer (constructor) method
    def __init__(self, name, age):
        self.name = name
        self.age = age

    # Method to make the dog bark
    def bark(self):
        return "Woof!"

    # Method to display the dog's age
    def display_age(self):
        return f"{self.name} is {self.age} years old."

